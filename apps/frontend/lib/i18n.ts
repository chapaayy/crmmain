export const translations: Record<Locale, Record<string, string>> = {
  en: {},
  ru
};

export function normalizeLocale(value?: string | null): Locale {
  return supportedLocales.includes(value as Locale) ? (value as Locale) : defaultLocale;
}

export function translate(value: string, locale: Locale) {
  return translations[locale][value] ?? value;
}

export function translationMapFor(locale: Locale) {
  if (locale === "ru") {
    return ru;
  }

  return Object.fromEntries(Object.entries(ru).map(([source, target]) => [target, source]));
}
